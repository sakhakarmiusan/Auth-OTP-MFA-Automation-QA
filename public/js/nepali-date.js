    const DAY_NAMES_EN = ['Sun','Mon','Tue','Wed','Thu','Fri','Sat'];
    const NP_DIGITS = ['०','१','२','३','४','५','६','७','८','९'];

    function toNepaliNumeral(num) {
        return String(num).split('').map(d => NP_DIGITS[parseInt(d)]).join('');
    }

    function getTotalDaysInBsYear(year) {
        if (!BS_MONTHS[year]) return 0;
        return BS_MONTHS[year].reduce((a, b) => a + b, 0);
    }

    function getDaysInBsMonth(year, month) {
        if (!BS_MONTHS[year]) return 30;
        return BS_MONTHS[year][month - 1] || 30;
    }

    function daysBetweenAD(d1, d2) {
        const t1 = Date.UTC(d1.getFullYear(), d1.getMonth(), d1.getDate());
        const t2 = Date.UTC(d2.getFullYear(), d2.getMonth(), d2.getDate());
        return Math.floor((t2 - t1) / 86400000);
    }

    function adToBs(adDate) {
        const ad = new Date(adDate.getFullYear(), adDate.getMonth(), adDate.getDate());
        let totalDays = daysBetweenAD(AD_REF, ad);
        if (totalDays < 0) return null;

        let bsYear = BS_YEAR_START;
        let bsMonth = 1;
        let bsDay = 1;

        while (totalDays > 0) {
            const daysInMonth = getDaysInBsMonth(bsYear, bsMonth);
            if (totalDays >= daysInMonth) {
                totalDays -= daysInMonth;
                bsMonth++;
                if (bsMonth > 12) { bsMonth = 1; bsYear++; }
            } else {
                bsDay += totalDays;
                totalDays = 0;
            }
        }
        return { year: bsYear, month: bsMonth, day: bsDay };
    }

    function bsToAd(bsYear, bsMonth, bsDay) {
        let totalDays = 0;
        for (let y = BS_YEAR_START; y < bsYear; y++) {
            totalDays += getTotalDaysInBsYear(y);
        }
        for (let m = 1; m < bsMonth; m++) {
            totalDays += getDaysInBsMonth(bsYear, m);
        }
        totalDays += bsDay - 1;
        const result = new Date(AD_REF);
        result.setDate(result.getDate() + totalDays);
        return result;
    }

    function getFirstDayOfMonth(bsYear, bsMonth) {
        const adDate = bsToAd(bsYear, bsMonth, 1);
        return adDate.getDay(); // 0=Sun
    }

    function getToday() {
        return adToBs(new Date());
    }

